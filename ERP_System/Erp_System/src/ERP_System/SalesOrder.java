    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        dname = jTextField4.getText();
        String sql = "Select D_ID from debtor where D_Name = '" + dname + "' and C_ID = " + cid;
        try {
            stmt = con.createStatement();
            rs = stmt.executeQuery(sql);
            if (rs.next()) {
                did = rs.getInt(1);
                quant = Integer.parseInt(jTextField5.getText());
                if(quant<q1){
                disc = Integer.parseInt(jTextField7.getText());
                tax = Integer.parseInt(jTextField6.getText());
                price = Double.parseDouble(jTextField8.getText());
                total = price * quant;
                total = total - ((double)disc/100 * total);
                total = total + ((double)tax/100 * total);
                JOptionPane.showMessageDialog(null, "Total = " + total);
                String query = "Insert Into Sales_order (C_ID, D_ID, Product, Quantity, Discount, Price) values (?,?,?,?,?,?)";
                pst = con.prepareStatement(query);
                pst.setInt(1, cid);
                pst.setInt(2, did);
                pst.setString(3,prod);
                pst.setInt(4, quant);
                pst.setInt(5, disc);
                pst.setDouble(6, total);
                if(!pst.execute())
                {
                    JOptionPane.showMessageDialog(null, "Record inserted");
                }
                else
                {
                    JOptionPane.showMessageDialog(null, "Error");
                }
                }else{
                    JOptionPane.showMessageDialog(null, "Invalid quantity");
                }
            }else{
                JOptionPane.showMessageDialog(null, "Invalid debtor name");
            }
        } catch (SQLException ex) {
            Logger.getLogger(SalesOrder.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

